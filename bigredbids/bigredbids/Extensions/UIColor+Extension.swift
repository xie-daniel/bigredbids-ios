import UIKit

extension UIColor {

    static let brb = Brb()

    struct Brb {
        let white = UIColor(red: 1, green: 1, blue: 1, alpha: 1)
        let gray = UIColor(red: 0.47, green: 0.47, blue: 0.47, alpha: 1)
        let lightGray = UIColor(red: 0.8, green: 0.8, blue: 0.8, alpha: 1)
        let black = UIColor(red: 0.12, green: 0.12, blue: 0.13, alpha: 1)
        let red = UIColor(red: 0.75, green: 0.06, blue: 0.06, alpha: 1)
        let crimson = UIColor(red: 0.69, green: 0.1, blue: 0.1, alpha: 1)
        let pink = UIColor(red: 1, green: 0.4, blue: 0.4, alpha: 1)
    }

}
